package by.koroza.programming_with_classes.classes.numberfive;

public class Count {
	private int value;

	public Count() {
		this.value = 0;
	}

	public Count(int value) {
		this.value = value;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}